# Step 1 : Import Libraries
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import (
    confusion_matrix,
    accuracy_score,
    classification_report
)

# Step 2 : Load Dataset
data = pd.read_csv(
    'https://github.com/ybifoundation/Dataset/raw/main/Credit%20Default.csv'
)

# Step 3 : Display First 5 Rows
print("First 5 Rows of Dataset:\n")
print(data.head())

# Step 4 : Dataset Information
print("\nDataset Information:\n")
print(data.info())

# Step 5 : Statistical Summary
print("\nDataset Description:\n")
print(data.describe())

# Step 6 : Check Default Counts
print("\nDefault Value Counts:\n")
print(data['Default'].value_counts())

# Step 7 : Define Features (X) and Target (y)
X = data.drop(['Default'], axis=1)
y = data['Default']

# Step 8 : Train-Test Split
X_train, X_test, y_train, y_test = train_test_split(
    X,
    y,
    train_size=0.7,
    random_state=2529
)

# Step 9 : Check Shapes
print("\nTraining and Testing Shapes:\n")
print("X_train shape :", X_train.shape)
print("X_test shape  :", X_test.shape)
print("y_train shape :", y_train.shape)
print("y_test shape  :", y_test.shape)

# Step 10 : Create Logistic Regression Model
model = LogisticRegression(max_iter=1000)

# Step 11 : Train Model
model.fit(X_train, y_train)

# Step 12 : Model Intercept
print("\nModel Intercept:\n")
print(model.intercept_)

# Step 13 : Model Coefficients
print("\nModel Coefficients:\n")
print(model.coef_)

# Step 14 : Prediction
y_pred = model.predict(X_test)

# Step 15 : Predicted Values
print("\nPredicted Values:\n")
print(y_pred)

# Step 16 : Confusion Matrix
print("\nConfusion Matrix:\n")
print(confusion_matrix(y_test, y_pred))

# Step 17 : Accuracy Score
print("\nAccuracy Score:\n")
print(accuracy_score(y_test, y_pred))

# Step 18 : Classification Report
print("\nClassification Report:\n")
print(classification_report(y_test, y_pred))

